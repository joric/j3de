void DrawSomething(void)
{
  int i,j;
  int vertices, faces;

  for(i=0;i<num_meta_balls;i++)
  {

    nlVector3D &c=meta_balls[i].center;

    c.x=(float)(sin(alpha*PI/(i+1)*1)*(X_RIGHT-X_LEFT)/4);
    c.y=(float)(sin(alpha*PI/(i+1)*2)*(Y_RIGHT-Y_LEFT)/4);
    c.z=(float)(sin(alpha*PI/(i+1)*3)*(Z_RIGHT-Z_LEFT)/4);

  }
  
	CalculateMetaBalls();

	nlCompositeVector2D v[3];

	vertices=0;
	faces=0;

  for(i=0;i<triangles_tesselated;i++)
  {
    Triangle& t=triangles_array[i];

    for(j=0;j<3;j++)
    {
      int m=j;

			v[j].x=t.p[m].x;
			v[j].y=t.p[m].y;
			v[j].z=t.p[m].z;

      nlVector3D n(0,0,0);
      for(int k=0;k<num_meta_balls;k++)
      {
        nlVector3D rv(
		  t.p[m].x-meta_balls[k].center.x,
          t.p[m].y-meta_balls[k].center.y,
          t.p[m].z-meta_balls[k].center.z);

        float r=!rv;
        float c=charge/(r*r*r);

        n+=rv*c;
      }

      n=n.Unit();

      v[j].u=map(n.x);
      v[j].v=map(n.y);      

		mv[vertices].vert.x=v[j].x;
		mv[vertices].vert.y=v[j].y;
		mv[vertices].vert.z=v[j].z;

		mv[vertices].norm.x=n.x;
		mv[vertices].norm.y=n.y;
		mv[vertices].norm.z=n.z;

		mv[vertices].u=v[j].u;
		mv[vertices].v=v[j].v;

		mf[faces].p[j]=&mv[vertices];

		mv[vertices].id=0;
		mv[vertices].flags=0;

		vertices++;	

		}

		c_FACE *face=&mf[faces];

		face->a = vertices-3;
		face->b = vertices-2;
		face->c = vertices-1;

		face->mat=0;
		face->flags=0;
	
		//calculate facenormal (for Clax)
		c_VECTOR  a, b, normal;

		vec_sub (&face->p[0]->vert,
			     &face->p[1]->vert, &a);

		vec_sub (&face->p[1]->vert,
				 &face->p[2]->vert, &b);

		vec_cross (&a, &b, &normal);
		vec_normalize (&normal, &face->norm);

		faces++;
  }
  
  
}

